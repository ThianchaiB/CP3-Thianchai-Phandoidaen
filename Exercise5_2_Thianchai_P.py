# ทำการคำนวณ v=s/t หาอัตราเร็ว ระยะทางที่เคลื่อนที่ได้ ต่อ 1 หน่วยเวลา
s = int(input("distance(m) :"))  # ระยะทางที่เคลื่อนที่ได้
t = int(input("time(s) :"))  # เวลาที่ใช้ในการเคลื่อนที่
v = s/t  # อัตราเร็ว ระยะทางที่เปลี่ยนไปต่อหน่อยเวลา
print("velocity :", v, "m/s")
print("velocity :", v*3600/1000, "km/h")
